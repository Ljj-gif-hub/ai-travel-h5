const PREFER = ['wikipedia','wikimedia','flickr','unsplash','pexels','pixabay','zhimg','bcebos','699pic','ctrip','qunar','mafengwo','gettyimages','shutterstock','500px']

function score(u) {
  const l = u.toLowerCase()
  for (const b of BLOCK) if (l.includes(b)) return 0
  let s = 30
  for (const p of PREFER) if (l.includes(p)) { s = 70; break }
  if (u.length < 150) s += 15
  if (u.length > 500) s -= 10
  if (l.endsWith('.gif')||l.endsWith('.svg')) s -= 40
  if (l.includes('.jpg')||l.includes('.jpeg')||l.includes('.png')||l.includes('.webp')) s += 5
  return Math.max(0, Math.min(100, s))
}

async function scrapeBing(city) {
  const q = encodeURIComponent(`${city} 旅游景点 实拍风景`)
  const imgs = []
  for (const host of ['cn.bing.com','www.bing.com']) {
    try {
      const r = await axios.get(`https://${host}/images/search?q=${q}&first=1`,{
        headers:{'User-Agent':UA,'Accept':'text/html','Accept-Language':'zh-CN,zh;q=0.9'},timeout:12000
      })
      const $ = cheerio.load(r.data)
      $('a.iusc').each((_,el)=>{
        try{const m=JSON.parse($(el).attr('m')||'{}');if(m.murl)imgs.push({url:m.murl,title:m.t||''})}catch{}
      })
      $('img.mimg').each((_,el)=>{
        const s=$(el).attr('src')||$(el).attr('data-src');if(s?.startsWith('http')&&!imgs.find(x=>x.url===s))imgs.push({url:s,title:$(el).attr('alt')||''})
      })
    }catch{}
  }
  return imgs
}

async function main(){
  const existing = fs.existsSync(OUT) ? JSON.parse(fs.readFileSync(OUT,'utf-8')) : {}
  const need = ALL.filter(c => !existing[c] || !existing[c].startsWith('http'))
  console.log(`${ALL.length}城市, 已爬${Object.keys(existing).filter(k=>existing[k]).length}, 待爬${need.length}\n`)

  let ok=0
  for(let i=0;i<need.length;i++){
    const city=need[i]
    process.stdout.write(`[${i+1}/${need.length}] ${city} `)
    const imgs = await scrapeBing(city)
    const best = imgs.map(x=>({...x,s:score(x.url)})).filter(x=>x.s>=50).sort((a,b)=>b.s-a.s)
    if(best.length>0){existing[city]=best[0].url;ok++;console.log(`✓ ${best[0].s}分`)}
    else{existing[city]='';console.log('✗')}
    if((i+1)%30===0){fs.writeFileSync(OUT,JSON.stringify(existing,null,2));console.log(`  → 存 | ✓${ok}\n`)}
    await new Promise(r=>setTimeout(r,800+Math.random()*400))
  }
  fs.writeFileSync(OUT,JSON.stringify(existing,null,2))
  const v=Object.values(existing).filter(Boolean).length
  console.log(`\n完成! ${ALL.length}城市, ${v}有图, ${ALL.length-v}无图`)
}
main().catch(console.error)
